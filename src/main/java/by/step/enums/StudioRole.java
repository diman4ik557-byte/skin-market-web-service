package by.step.enums;

/**
 * Перечисление возможных ролей участников студии.
 * Определяет права и обязанности внутри студии.
 *
 * @author Skin Market Team
 * @version 1.0
 */
public enum StudioRole {

    /**
     * Художник - обычный участник студии.
     * Выполняет заказы, полученные через студию.
     */
    ARTIST,

    /**
     * Менеджер - управляет студией.
     * Может принимать/отклонять заявки, распределять заказы, управлять участниками.
     */
    MANAGER,

    /**
     * Основатель - создатель студии.
     * Имеет все права менеджера и дополнительные привилегии.
     */
    FOUNDER
}