package by.step.enums;

/**
 * Перечисление возможных статусов заказа.
 * Определяет жизненный цикл заказа от создания до завершения или отмены.
 *
 * @author Skin Market Team
 * @version 1.0
 */
public enum OrderStatus {

    /**
     * Новый заказ, только создан.
     * Художник может принять или отклонить заказ.
     */
    NEW,

    /**
     * Заказ в работе.
     * Художник начал выполнение заказа.
     */
    IN_PROGRESS,

    /**
     * Заказ на проверке.
     * Художник отправил готовую работу на проверку заказчику.
     */
    REVIEW,

    /**
     * Заказ завершен.
     * Заказчик подтвердил выполнение заказа.
     */
    COMPLETED,

    /**
     * Заказ отменен.
     * Может быть отменен заказчиком или художником до начала выполнения.
     */
    CANCELLED
}